import turtle   # uključi modul za crtanje
turtle.clear()  # pobriši prozor za iscrtavanje
turtle.up()     # podigni olovku
turtle.goto(40, 40) # pomakni kornjaču (bez ostavljanja traga)
turtle.down()   # spusti olovku
turtle.goto(60, 60) # pomakni kornjaču uz iscrtavanje traga pomaka
turtle.goto(40, 80) # pomakni kornjaču uz iscrtavanje traga pomaka
turtle.goto(20, 60) # pomakni kornjaču uz iscrtavanje traga pomaka
turtle.goto(40, 40) # pomakni kornjaču uz iscrtavanje traga pomaka
turtle.done()